let negara = "Indonesia"
let sapaan = null

switch (negara) {
    case "Indonesia":
        sapaan = "Selamat Pagi!"
        break
    case "Inggris":
        sapaan = "Good Morning!"
        break
    case "Jepang":
        sapaan = "Ohayou Gozaimasu!"
        break
    case "Jerman":
        sapaan = "Guten Morgen!"
        break
    default:
        sapaan = "Selamat Pagi!"
}
console.log(sapaan)
